<?
namespace pages\main;

class ControllerMain extends \core\Controller
	{
	/* -------------------------------------------------------------------- */
	/* ---------------------------- constructor --------------------------- */
	/* -------------------------------------------------------------------- */
	public function __construct()
		{
		$this->setModel(new ModelMain);
		$this->setView(new ViewMain);
		}
	/* -------------------------------------------------------------------- */
	/* ------------------------------ start ------------------------------- */
	/* -------------------------------------------------------------------- */
	public function start()
		{
		$this->actionIndex();
		}
	/* -------------------------------------------------------------------- */
	/* --------------------------- action index --------------------------- */
	/* -------------------------------------------------------------------- */
	public function actionIndex()
		{
		$this->getView()->outputPage();
		}
	}